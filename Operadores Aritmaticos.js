
/*
Operadores Aritméticos

    +  -> Adição
    -  -> Subtração
    /  -> Divisão
    *  -> Multiplicação
    %  -> Resto
    ++ -> Incremento
    -- -> Decremento
    ** -> Exponencial

*/

const adição = 3 + 2
console.log(adição)
console.log("---------------------------------")

const subtração = 3 - 2
console.log(subtração)
console.log("---------------------------------")

const divisão = 3 / 2
console.log(divisão)
console.log("---------------------------------")

const multiplicação = 3 * 2
console.log(multiplicação)
console.log("---------------------------------")

const resto = 3 / 2
console.log(resto)
console.log("---------------------------------")

let incremento = 20
incremento ++
console.log(incremento)
//ou
console.log(++incremento)
console.log("---------------------------------")

let decremento = 20
decremento --
console.log(decremento)
// ou
console.log(--decremento)
console.log("---------------------------------")

let exponencial = 3 ** 3  // potencia
console.log(exponencial)